package java_program;

public class PrimitiveDataType {

	public static void main(String[] args) {
		
		// Primitive DataType
  		
  		// Numeric Value 
		
		long  number4  = 1234567; // 8 byte
		
		int number3 = 100000;   // 4 byte
		
		short number2 = 32600;  // 2 byte
		
		byte number1 = 127;  // 1 byte
  		
  		System.out.println(number4);
  		
  		System.out.println(number3);
  		
  		System.out.println(number2);
  		
  		System.out.println(number1);
  		
  		// Decimal Value
  		
  		double number5 = 12.5;   // 8 byte
  		
  		float  number6 = 25.5f;   // 4 byte
  		 
  		System.out.println(number6);
  		
  		System.out.println(number5);
  		
  	   // True and False
  		
  		boolean is_logo_displayed = true; // 1 bit
  		
  		boolean is_logo_not_displayed = false;  // 1 bit
  		
  		System.out.println(is_logo_displayed);
  		
  		System.out.println(is_logo_not_displayed);
  		
  		// Single Character
 
  		char alphabet ='a';    // 2 byte 
  		
  		System.out.println(alphabet);
  		
  		
  	     // Primitive data type is fix size
  		
        // DataType           //Variable          //AssignmentOprator     //value     //StatementTarminator
  		
  		   int                   num1                      =                  1599            ;
  		
  		 System.out.println(num1);
  		 
  		 
	}	

}
