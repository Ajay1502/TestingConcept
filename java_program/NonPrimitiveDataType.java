package java_program;

public class NonPrimitiveDataType {

	public static void main(String[] args) {
		
		//String
		
		String info = "Ajay pande no 8554843552";
		
		System.out.println(info);
		
		String info1 = "sushant gohil no 8888812705";
		
		System.out.println(info1);
		
		// Array []
		
		int empNumber  = 282;
		 
		int empNumber1  = 6867;
		
		int empNumber2 = 6473;
		
		System.out.println(empNumber);
		
		System.out.println(empNumber1);
		
		System.out.println(empNumber2);
		
		//Array = It collection of similar type objects
		
		int [] empNumbers = {3546,7867,1231,6787};   //Primitive
	 	                    // 0    1    2    3     = Array count starting is 0
		
		System.out.println(empNumbers[0]);
		
		System.out.println(empNumbers[1]);
		
		System.out.println(empNumbers[2]);
		
		System.out.println(empNumbers[3]);
		
		String [] empNames = {"ajay,sushant,nilesh,rushi"}; // NonPrimitive
		
		System.out.println(empNames[0]);
		
		System.out.println(empNames[1]);
		
		System.out.println(empNames[2]);
		
		System.out.println(empNames[3]);
		
		//NonPrimitive data type size not fix.
		
		
	}

}
